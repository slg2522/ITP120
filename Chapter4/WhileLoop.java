package allFiles;

/**While is a pretest loop, meaning it tests its expression before each iteration. */

public class WhileLoop
{
	public static void main(String[] args)
	{
		int number = 1;
		
		while (number <= 5)
		{
			System.out.println("Hello");
			number++;
		}
		System.out.println("All Done!");
	}
}
