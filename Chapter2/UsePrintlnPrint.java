package allFiles;

public class UsePrintlnPrint
{
	public static void main(String[] args)
	{
		//print is all on the same line
		System.out.print("Programming is ");
		System.out.print("great fun!");
		
		//moves vs. to a new line using \n
		System.out.println("\n    vs.   ");
		
		//print is on 2 separate lines
		System.out.println("Programming is ");
		System.out.println("great fun!");
		
		//print with tabs
		System.out.println("");
		System.out.println("Programming");
		System.out.println("\t is");
		System.out.println("\t\t great");
		System.out.println("\t\t\t fun!");
	}
}
