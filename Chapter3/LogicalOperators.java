package allFiles;

public class LogicalOperators
{
	//Logical Operators
	/*&&		AND		Connects 2 boolean expression into one. Both expressions must be true for the overall expression to be True.
	 * ||		OR		Only one must be True for the expression to be True.
	 * !		NOT		Reverses the Truth of an expression (ex: if False, returns True)
	 */

	//Example:
	//x > y && a < b		Is x greater than y and a less than b?
	//x == y || x == z	Is x equal to y or is x equal to z?
	//!(x > y)			Is x not greater than y?
	
	/*Example:
	 * if (salary >= 50000 || yearsOnJob >= 2)
	 * {
	 * 		System.out.println("You qualify for the loan.");
	 * {
	 * else
	 * {
	 * 	System.out.println("You do not qualify.");
	 * }
	 * 
	 * Logical Operators in Order of Precedence:
	 * !
	 * &&
	 * ||
	 * 
	 * Precedence of all operators discussed so far
	 * 1		- (unary negation) !	Unary negation, logical NOT */
	 // 2		*/ %					Multiplication, Division, Modulus
	 // 3		+ -						Addition, Subtraction
	 // 4		< > <= >=				Less than, Greater than, Less than or equal to, Greater than or equal to
	 // 5		= !=					Equal to, not equal to
	 // 6		&&						Logical AND
	 // 7		||						Logical OR
	 // 8		= += -= *= /= %=		Assignment & combined assignment
}
